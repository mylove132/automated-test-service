import {
    Column,
    CreateDateColumn,
    Entity,
    ManyToOne,
    OneToMany,
    PrimaryGeneratedColumn,
    UpdateDateColumn
} from "typeorm";
import {CaseEntity} from "../case/case.entity";
import {EnvEntity} from "../env/env.entity";
import {PlatformCodeEntity} from "../catalog/platformCode.entity";


@Entity('token')
export class TokenEntity {

    @PrimaryGeneratedColumn()
    id: number;

    @Column({comment: "用户名称",nullable: true})
    username: string;

    @ManyToOne(type => PlatformCodeEntity, platformCode => platformCode.tokens,{cascade: true,onDelete: 'CASCADE',onUpdate: 'CASCADE'})
   platformCode: PlatformCodeEntity;

    @Column({comment:"登录数据", nullable: true})
    body: string;

    @Column('text',{comment: "token",nullable: true})
    token: string;

    @Column({comment: "url"})
    url: string;

    @CreateDateColumn()
    createDate: Date;

    @UpdateDateColumn()
    updateDate: Date;

    @OneToMany(type => CaseEntity,cases => cases.token)
    cases: CaseEntity;

    @ManyToOne(type => EnvEntity, env => env.tokens,{cascade: true,onDelete: 'CASCADE',onUpdate: 'CASCADE'})
    env: EnvEntity;


}
















































































































































































































































































































































































































































































































