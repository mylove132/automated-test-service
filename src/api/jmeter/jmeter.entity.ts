import {Column, CreateDateColumn, Entity, PrimaryGeneratedColumn, UpdateDateColumn} from 'typeorm';

@Entity('jmeter')
export class JmeterEntity {

    @PrimaryGeneratedColumn()
    id: number;

    @CreateDateColumn()
    createDate: Date;

    @UpdateDateColumn()
    updateDate: Date;

    //压测时长（ms）
    @Column()
    preCountTime: number;

    //压测并发数
    @Column()
    preCountNumber: number;

    //压测结果ID
    @Column()
    preResultId: string;

}
















































































































































































































































































































































































































































































































