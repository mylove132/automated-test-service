import {Column, Entity, OneToMany, PrimaryGeneratedColumn} from 'typeorm';
import {CaseEntity} from "./case.entity";



@Entity('assert_type')
export class AssertTypeEntity {

    @PrimaryGeneratedColumn()
    id: number;

    @Column({comment: "断言类型"})
    type: string;

    @OneToMany(type => CaseEntity, cases => cases.assertType)
    cases: CaseEntity[];
}


@Entity('assert_judge')
export class AssertJudgeEntity {

    @PrimaryGeneratedColumn()
    id: number;

    @Column({comment: "断言类型"})
    name: string;

    @OneToMany(type => CaseEntity, cases => cases.assertJudge)
    cases: CaseEntity[];
}














































































































































































































































































































































































































































































































